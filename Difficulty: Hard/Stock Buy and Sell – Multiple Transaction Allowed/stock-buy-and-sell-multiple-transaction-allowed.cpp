class Solution {
  public:
    int maximumProfit(vector<int> &p ) {
        // code here
        int profit = 0;
        for(int i = 0; i < p.size() - 1; i++ )
        {
            if( p[i + 1] > p[i] )
            {
                profit += ( p[i + 1] - p[i] );
            }
        }
        
        return profit;
    }
};


